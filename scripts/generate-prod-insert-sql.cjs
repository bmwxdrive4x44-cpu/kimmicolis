/**
 * Reads the 5 real local users (and their relais/applications)
 * and generates a SQL file with INSERT ... ON CONFLICT DO UPDATE statements
 * ready to be executed on prod via: prisma db execute --file <output>
 */
const { PrismaClient } = require('../src/generated/prisma');
const fs = require('fs');
const path = require('path');

const LOCAL_EMAILS = [
  'admin@swiftcolis.dz',
  'shopteek@gmail.com',
  'bmwxdrive44@gmail.com',
  'soniatrans44@gmail.com',
  'bmwxdrive4x44@gmail.com',
];

function escape(v) {
  if (v === null || v === undefined) return 'NULL';
  if (typeof v === 'boolean') return v ? 'TRUE' : 'FALSE';
  if (typeof v === 'number') return String(v);
  if (v instanceof Date) return `'${v.toISOString()}'`;
  // String — escape single quotes
  return `'${String(v).replace(/'/g, "''")}'`;
}

function escapeArray(arr) {
  if (!arr || arr.length === 0) return "'{}'";
  const items = arr.map((v) => `"${String(v).replace(/"/g, '\\"')}"`).join(',');
  return `'{${items}}'`;
}

function val(v) {
  if (Array.isArray(v)) return escapeArray(v);
  return escape(v);
}

async function main() {
  const outputPath = path.resolve(__dirname, '../scripts/prod-insert-local-users.sql');
  const localUrl = process.env.SOURCE_DATABASE_URL || process.env.DATABASE_URL;

  if (!localUrl) {
    console.error('SOURCE_DATABASE_URL is required.');
    process.exit(1);
  }

  const local = new PrismaClient({ datasources: { db: { url: localUrl } } });

  try {
    const users = await local.user.findMany({
      where: { email: { in: LOCAL_EMAILS, mode: 'insensitive' } },
      orderBy: { createdAt: 'asc' },
      select: {
        id: true, email: true, password: true, name: true,
        firstName: true, lastName: true, address: true, role: true,
        phone: true, siret: true, image: true, isActive: true,
        clientType: true, eligibleProImplicit: true, eligibleProSince: true,
        proLastEvaluatedAt: true, weeklyValidShipments: true,
        createdAt: true, updatedAt: true,
      },
    });

    if (users.length === 0) {
      console.error('Aucun utilisateur local trouvé pour les emails spécifiés.');
      process.exit(1);
    }

    const userIds = users.map((u) => u.id);

    const relaisList = await local.relais.findMany({
      where: { userId: { in: userIds } },
      select: {
        id: true, userId: true, commerceName: true, address: true,
        ville: true, openTime: true, closeTime: true, latitude: true, longitude: true,
        photos: true, commerceDocuments: true,
        commissionPetit: true, commissionMoyen: true, commissionGros: true,
        status: true, operationalStatus: true, suspensionReason: true, suspendedAt: true,
        cashCollected: true, cashReversed: true, cautionAmount: true,
        cautionStatus: true, cautionPaidAt: true, activationDate: true,
        firstActivityDate: true, complianceScore: true, createdAt: true, updatedAt: true,
      },
    });

    const applications = await local.transporterApplication.findMany({
      where: { userId: { in: userIds } },
      select: {
        id: true, userId: true, fullName: true, phone: true, vehicle: true,
        license: true, experience: true, regions: true, description: true,
        documents: true, status: true, createdAt: true, updatedAt: true,
      },
    });

    const lines = [];
    lines.push('-- Auto-generated by scripts/generate-prod-insert-sql.cjs');
    lines.push(`-- Generated at ${new Date().toISOString()}`);
    lines.push('-- Users to insert/update: ' + users.map((u) => u.email).join(', '));
    lines.push('');

    // ---- USERS ----
    for (const u of users) {
      lines.push(`INSERT INTO "User" (id, email, password, name, "firstName", "lastName", address, role, phone, siret, image, "isActive", "clientType", "eligibleProImplicit", "eligibleProSince", "proLastEvaluatedAt", "weeklyValidShipments", "createdAt", "updatedAt")`);
      lines.push(`VALUES (`);
      lines.push(`  ${val(u.id)}, ${val(u.email)}, ${val(u.password)}, ${val(u.name)},`);
      lines.push(`  ${val(u.firstName)}, ${val(u.lastName)}, ${val(u.address)}, ${val(u.role)},`);
      lines.push(`  ${val(u.phone)}, ${val(u.siret)}, ${val(u.image)}, ${val(u.isActive)},`);
      lines.push(`  ${val(u.clientType)}, ${val(u.eligibleProImplicit)}, ${val(u.eligibleProSince)},`);
      lines.push(`  ${val(u.proLastEvaluatedAt)}, ${val(u.weeklyValidShipments)}, ${val(u.createdAt)}, ${val(u.updatedAt)}`);
      lines.push(`)`);
      lines.push(`ON CONFLICT (email) DO UPDATE SET`);
      lines.push(`  name = EXCLUDED.name,`);
      lines.push(`  password = EXCLUDED.password,`);
      lines.push(`  "firstName" = EXCLUDED."firstName",`);
      lines.push(`  "lastName" = EXCLUDED."lastName",`);
      lines.push(`  address = EXCLUDED.address,`);
      lines.push(`  role = EXCLUDED.role,`);
      lines.push(`  phone = EXCLUDED.phone,`);
      lines.push(`  "isActive" = EXCLUDED."isActive",`);
      lines.push(`  "clientType" = EXCLUDED."clientType",`);
      lines.push(`  "updatedAt" = EXCLUDED."updatedAt";`);
      lines.push('');
    }

    // ---- RELAIS ----
    for (const r of relaisList) {
      lines.push(`INSERT INTO "Relais" (id, "userId", "commerceName", address, ville, "openTime", "closeTime", latitude, longitude, photos, "commerceDocuments", "commissionPetit", "commissionMoyen", "commissionGros", status, "operationalStatus", "suspensionReason", "suspendedAt", "cashCollected", "cashReversed", "cautionAmount", "cautionStatus", "cautionPaidAt", "activationDate", "firstActivityDate", "complianceScore", "createdAt", "updatedAt")`);
      lines.push(`VALUES (`);
      lines.push(`  ${val(r.id)}, ${val(r.userId)}, ${val(r.commerceName)}, ${val(r.address)}, ${val(r.ville)},`);
      lines.push(`  ${val(r.openTime)}, ${val(r.closeTime)}, ${val(r.latitude)}, ${val(r.longitude)},`);
      lines.push(`  ${val(r.photos)}, ${val(r.commerceDocuments)},`);
      lines.push(`  ${val(r.commissionPetit)}, ${val(r.commissionMoyen)}, ${val(r.commissionGros)},`);
      lines.push(`  ${val(r.status)}, ${val(r.operationalStatus)}, ${val(r.suspensionReason)}, ${val(r.suspendedAt)},`);
      lines.push(`  ${val(r.cashCollected)}, ${val(r.cashReversed)}, ${val(r.cautionAmount)},`);
      lines.push(`  ${val(r.cautionStatus)}, ${val(r.cautionPaidAt)}, ${val(r.activationDate)},`);
      lines.push(`  ${val(r.firstActivityDate)}, ${val(r.complianceScore)}, ${val(r.createdAt)}, ${val(r.updatedAt)}`);
      lines.push(`)`);
      lines.push(`ON CONFLICT (id) DO UPDATE SET`);
      lines.push(`  "commerceName" = EXCLUDED."commerceName",`);
      lines.push(`  address = EXCLUDED.address,`);
      lines.push(`  ville = EXCLUDED.ville,`);
      lines.push(`  "openTime" = EXCLUDED."openTime",`);
      lines.push(`  "closeTime" = EXCLUDED."closeTime",`);
      lines.push(`  status = EXCLUDED.status,`);
      lines.push(`  "operationalStatus" = EXCLUDED."operationalStatus",`);
      lines.push(`  "updatedAt" = EXCLUDED."updatedAt";`);
      lines.push('');
    }

    // ---- TRANSPORTER APPLICATIONS ----
    for (const a of applications) {
      lines.push(`INSERT INTO "TransporterApplication" (id, "userId", "fullName", phone, vehicle, license, experience, regions, description, documents, status, "createdAt", "updatedAt")`);
      lines.push(`VALUES (`);
      lines.push(`  ${val(a.id)}, ${val(a.userId)}, ${val(a.fullName)}, ${val(a.phone)}, ${val(a.vehicle)},`);
      lines.push(`  ${val(a.license)}, ${val(a.experience)}, ${val(a.regions)}, ${val(a.description)},`);
      lines.push(`  ${val(a.documents)}, ${val(a.status)}, ${val(a.createdAt)}, ${val(a.updatedAt)}`);
      lines.push(`)`);
      lines.push(`ON CONFLICT (id) DO UPDATE SET`);
      lines.push(`  status = EXCLUDED.status,`);
      lines.push(`  "updatedAt" = EXCLUDED."updatedAt";`);
      lines.push('');
    }

    fs.writeFileSync(outputPath, lines.join('\n'), 'utf8');
    console.log(`SQL generated: ${outputPath}`);
    console.log(`Users: ${users.length}, Relais: ${relaisList.length}, Applications: ${applications.length}`);
    users.forEach((u) => console.log(`  [${u.role}] ${u.email}`));
  } finally {
    await local.$disconnect();
  }
}

main().catch((e) => { console.error(e); process.exit(1); });
